// Class SumOneToTen computes sum of numbers from 1 to 10
class SumOneToTen {
    public static void main(String[] args) {
        int sum = 0;

        // Loop 1 to 10
        for (int i = 1; i <= 10; i++) {
            sum += i;
        }

        System.out.println("Sum of numbers from 1 to 10 = " + sum);
    }
}
